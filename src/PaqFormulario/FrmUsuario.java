    private void txtUsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtUsuActionPerformed
        try {
            String sql = "SELECT * FORM usuario WHERE usu_nic='" + txtUsu.getText() + "'";
            //System.out.println(sql);
            rs = con.Consulta(sql);
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "''El nombre de usuario ya existe escoje otro por favor''");
                txtUsu.setText("");
                txtUsu.grabFocus();
            } else {
                this.txtPas.setEnabled(true);
                txtPas.grabFocus();
                txtUsu.setText(txtUsu.getText().toUpperCase());
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_txtUsuActionPerformed

    private void txtCodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCodActionPerformed
        String sql1 = "SELECT * FROM usuario WHERE  usu_cod=" + txtCod.getText();

        if (FrmPrincipal.isNumeric(txtCod.getText())) {
            if (validar(sql1) == true) {

                if (botonmodificar == true) {
                    try {
                        String sql = "Select * FROM usuario "
                                + "WHERE usu_cod=" + this.txtCod.getText();
                        //System.out.println(sql);
                        rs = con.Consulta(sql);
                        rs.next();
                        this.txtNom.setText(rs.getString("usu_nom"));
                        this.txtUsu.setText(rs.getString("usu_nic"));
                        this.txtNom.setEnabled(true);
                        txtNom.grabFocus();
                    } catch (SQLException ex) {
                        Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                this.txtCod.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "El Codigo No Existe");
                this.txtCod.setText("");
                this.txtCod.grabFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Solo puede Ingresar Numeros");
            this.txtCod.setText("");
            this.txtCod.grabFocus();
        }
}//GEN-LAST:event_txtCodActionPerformed

