    private void btnGraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraActionPerformed
        int res = JOptionPane.showConfirmDialog(null, "Realmente, ¿Desea Grabar"
                + " los Datos?", "Mensaje", JOptionPane.OK_CANCEL_OPTION);

        if (res == JOptionPane.OK_OPTION) {
            if (botonagregar == true) {
                String sql = "INSERT INTO usuario (usu_cod,usu_nom,usu_nic,usu_pas)"
                        + " VALUES (" + this.txtCod.getText() + ",'" + this.txtNom.getText() + "','"
                        + this.txtUsu.getText() + "','" + this.txtPas.getText() + "')";
                //System.out.println(sql);
                con.Guardar(sql);
                ActualizarGrilla();
                botonagregar = false;
            }
            if (botonmodificar == true) {
                String update = "UPDATE usuario SET "
                        + "usu_nom='" + this.txtNom.getText()
                        + "',usu_nic='" + this.txtUsu.getText()
                        + "', usu_pas='" + this.txtPas.getText()
                        + "' WHERE usu_cod=" + this.txtCod.getText();
                //System.out.println(update);
                con.Guardar(update);
                ActualizarGrilla();
                botonmodificar = false;
            }
            JOptionPane.showMessageDialog(null, "Los datos se guardaron correctamente");
            this.btnCan.doClick();
        } else {
            this.btnCan.doClick();
        }
}//GEN-LAST:event_btnGraActionPerformed

